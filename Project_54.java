/*
	Пример использования динамической инициализации;
	Пример с поиском длины гипотенузы;
 */
public class Project_54 {
	static void main() {
		double a = 3.0, b = 4.0;
		double c = Math.sqrt(a*a + b*b);                            // sqrt() - вычисление квадратного корня;
		System.out.println("Длина гипотенузы равна " + c);
	}
}
