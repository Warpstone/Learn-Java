/*
	Вычисляем площадь круга. Пример работы типа double;
 */
public class Project_50 {
	static void main() {
		double pi, r, a;
		r = 10.8;                                           // радиус круга;
		pi = 3.14159;                                       // приблизительное значение п;
		a = pi * r * r;                                     // вычисление площади;
		System.out.println("Площадь круга равна " + a);
	}
}
